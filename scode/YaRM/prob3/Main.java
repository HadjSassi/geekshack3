class Main {
    public static void main(String[] args) {
        int longeure = 0;
        int[][] matrice = new int[5][];
        for (int i = 0; i < matrice[i].length; i++) {
            for (int j = 0; j < matrice[j].length; j++) {
                //matrice[i] = new int[5];
                while (matrice[i][j] == 1){
                    longeure += 100;
                }
            }
        }System.out.println(longeure);
    }
}  